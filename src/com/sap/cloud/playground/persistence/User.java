package com.sap.cloud.playground.persistence;

public class User {
	 	private String id;
	    private String email;
	    
	    public String getId() {
	        return id;
	    }

	    public void setId(String newId) {
	        this.id = newId;
	    }

	    public String getEmail() {
			return email;
		}

		public void setEmail(String email) {
			this.email = email;
		}

}
